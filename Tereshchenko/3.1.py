# Инициализация переменных
total = 0

# Объявление цикла while
while True:
    # Запрос данных от пользователя
    num = int(input("Введите число (-1 для завершения): "))

    # Проверка условия выхода из цикла
    if num == -1:
        break

    # Тело цикла - накопление суммы
    total += num

# Вывод результата
print("Сумма введенных чисел:", total)