# Ввод исходных данных
num1 = float(input("Введите первое число: "))
num2 = float(input("Введите второе число: "))

# Вычисление результатов по формулам
sum_result = num1 + num2
diff_result = num1 - num2
prod_result = num1 * num2
quot_result = num1 / num2

# Вывод значений результатов
print("Результаты арифметических операций:")
print("Сумма:", sum_result)
print("Разность:", diff_result)
print("Произведение:", prod_result)
print("Частное:", quot_result)