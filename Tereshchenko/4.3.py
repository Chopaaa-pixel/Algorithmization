# Запрашиваем данные у пользователя
h = int(input("Введите высоту елки: "))
ch = input("Введите символ: ")

# Общая ширина строки для выравнивания (ствол из двух символов в середине)
# Ширина = (символы слева) + 2 (ствол) + (символы справа)
# Наибольшее количество символов слева/справа = высота - 1
max_chars_side = h - 1
full_width = 2 * max_chars_side + 2  # +2 для ствола

# Рисуем крону
for level in range(h):
    # Количество символов с каждой стороны на текущем уровне = level
    chars_count = level + 1  # Для первого уровня (level=0) будет 1 символ с каждой стороны
    # Строка символов слева
    left_side = ch * chars_count
    # Строка символов справа
    right_side = ch * chars_count
    # Собираем строку уровня
    level_str = left_side + '||' + right_side
    # Выравнивание по центру для симметричного отображения
    # Дополняем пробелами слева до общей ширины
    padding = (full_width - len(level_str)) // 2
    print(' ' * padding + level_str)

# Рисуем ствол
# Ствол состоит из двух вертикальных символов, выровненных по центру
trunk_str = '||'
padding_trunk = (full_width - len(trunk_str)) // 2
print(' ' * padding_trunk + trunk_str)