# 2.1 Создание списков
group1 = ["Иванов", "Петров", "Сидоров", "Козлов", "Смирнов",
          "Васильев", "Николаев", "Алексеев", "Фёдоров", "Дмитриев"]

group2 = ["Соколов", "Орлов", "Лебедев", "Волков", "Зайцев",
          "Медведев", "Егоров", "Павлов", "Семёнов", "Макаров"]

# 2.2 Создание спортивной команды (кортеж)
team = tuple(group1[:5] + group2[:5])

# 2.3 Вывод исходных данных
print("Исходные списки групп:")
print("Группа 1:", group1)
print("Группа 2:", group2)
print("Спортивная команда:", team)

# 2.4 Вывод длины кортежа
print("Длина кортежа:", len(team))

# 2.5 Сортировка кортежа по алфавиту
sorted_team = tuple(sorted(team))
print("Отсортированный кортеж:", sorted_team)

# 2.6 Проверка наличия "Иванов"
if "Иванов" in team:
    count = team.count("Иванов")
    print(f"Студент 'Иванов' входит в команду. Встречается {count} раз(а)")
else:
    print("Студент 'Иванов' не входит в команду")