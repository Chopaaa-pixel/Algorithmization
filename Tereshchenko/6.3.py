# Создание словаря
precipitation = {
    "Январь": 45,
    "Февраль": 40,
    "Март": 35,
    "Апрель": 50,
    "Май": 55,
    "Июнь": 60,
    "Июль": 65,
    "Август": 70,
    "Сентябрь": 55,
    "Октябрь": 50,
    "Ноябрь": 45,
    "Декабрь": 40
}

# Сортировка по возрастанию количества осадков
sorted_months = sorted(precipitation.items(), key=lambda x: x[1])

# Вывод отсортированных данных
print("Месяцы отсортированные по возрастанию осадков:")
for month, precip in sorted_months:
    print(f"{month}: {precip} мм")